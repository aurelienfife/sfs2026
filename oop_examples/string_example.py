# In python, strings are objects derived from a class called "str"
# They embed methods to transform and perform operations
data = "Hello, World!"

# data in this case is an object
# and the embedded operations are triggered using 
# the "dot" syntax
new_data = data.upper()
newer_data = data.lower()
newest_data = data.split()

print(new_data, newer_data, newest_data)

